    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        String gender = "";
        
        if(laki.isSelected()){
            gender = "Laki-Laki";
        }else if(perempuan.isSelected()){
            gender = "Perempuan";
        }
        
        if(txtnama.getText().equals("")||txtnim.getText().equals("")||gender.equals("")||txtalamat.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Data tidak lengkap!!!");
        
        }else{
        
        try {
            String data = "UPDATE identitas SET nim ='" + txtnim.getText()
                                          + "',nama ='" + txtnama.getText()
                                          + "',gender ='" + gender
                                          + "',alamat ='" + txtalamat.getText()
                                          + "'WHERE nim ='" + txtnim.getText() + "'";
            Connection con = (Connection) koneksi.getConnection();
            PreparedStatement pst = con.prepareStatement(data);
            pst.execute();
            
        DefaultTableModel tblModel = (DefaultTableModel)tabel.getModel();
        if(tabel.getSelectedRowCount()==1){
                      
            tblModel.setValueAt(txtnim.getText(), tabel.getSelectedRow(), 1);
            tblModel.setValueAt(txtnama.getText(), tabel.getSelectedRow(), 2);
            tblModel.setValueAt(gender, tabel.getSelectedRow(), 3);
            tblModel.setValueAt(txtalamat.getText(), tabel.getSelectedRow(), 4);
            
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!!!");
        }else{
            if(tabel.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Tabel kosong!!!");
            }else{
                JOptionPane.showMessageDialog(this, "Harap pilih satu data!!!");
                 }
             }

        reset();
        } catch (Exception e) {
        }
        }
    }//GEN-LAST:event_btneditActionPerformed

