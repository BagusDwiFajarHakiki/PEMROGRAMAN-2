    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here: 
        String gender = "";
        
        if(laki.isSelected()){
            gender = "Laki-laki";
        }else if(perempuan.isSelected()){
            gender = "Perempuan";
        }
        
        if(txtnama.getText().equals("")||txtnim.getText().equals("")||gender.equals("")||txtalamat.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Data tidak lengkap!!!");
        
        }else{
        try {
            String data = "INSERT INTO identitas VALUES ('" + txtnim.getText() + "','"
                                                            + txtnama.getText() + "','"
                                                            + gender + "','"
                                                            + txtalamat.getText()
                                                            + "')";
            Connection con = (Connection) koneksi.getConnection();
            PreparedStatement pst = con.prepareStatement(data);
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Berhasil disimpan");
            
            reset();
                        
        } catch (Exception e) {
           JOptionPane.showMessageDialog(null, "Gagal disimpan " + e.getMessage());
        }
        }
        tabel();
    }//GEN-LAST:event_btnsimpanActionPerformed

